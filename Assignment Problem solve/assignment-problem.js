function validEmail(email) {

    if (email[0] === '.' || email[0] === '_' || email[0] === '-' || email[0] === '@' || email[0] === '+') {
        return false;
    }
    else if (email.includes(' ')) {
        return false;
    }
    let atIndex = email.indexOf('@'); // find where '@' is
    if (atIndex === -1) {
        return false; // no '@' found
    }


    let lastPart = email.slice("-4");
    if (lastPart !== '.com') {
        return false;
    }

    return true;

}

